using System.Data;
using System.Data.SqlClient;
using System.Reflection.Metadata;

namespace users_api.Sources
{
    public class DBData
    {
        public static string dbUser = "test";
        public static string dbPassword = "test";
        public static string cadenaConexion = "Data Source=DESKTOP-NNV5NAH;Initial Catalog=DoubleVPartnersDb;User ID="+ dbUser+ ";Password="+ dbPassword ;
        public static DataSet ListarTablas(string nombreProcedimiento, List<Param> parametros = null)
        {
            SqlConnection conexion = new SqlConnection(cadenaConexion);

            try
            {
                conexion.Open();
                SqlCommand cmd = new SqlCommand(nombreProcedimiento, conexion);
                cmd.CommandType = System.Data.CommandType.StoredProcedure;

                if (parametros != null)
                {
                    foreach (var parametro in parametros)
                    {
                        cmd.Parameters.AddWithValue(parametro.name, parametro.value);
                    }
                }
                DataSet tabla = new DataSet();
                SqlDataAdapter da = new SqlDataAdapter(cmd);
                da.Fill(tabla);


                return tabla;
            }
            catch (Exception ex)
            {
                return null;
            }
            finally
            {
                conexion.Close();
            }
        }

